#include <unistd.h>

void	_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_numbers(void)
{
	int	i;

	i = 0;
	while (i <= 9)
	{
		_putchar(i + '0');
		i++;
	}
}
